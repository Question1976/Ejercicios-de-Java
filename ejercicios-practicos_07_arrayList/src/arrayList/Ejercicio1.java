package arrayList;

import java.util.ArrayList;

public class Ejercicio1 {

	public static void main(String[] args) {
		
		// Ejercicio 1. 1. Crear y añadir elementos a un ArrayList.
		
        // Crear un ArrayList para almacenar nombres
        ArrayList<String> nombres = new ArrayList<>();

        // Añadir elementos al ArrayList
        nombres.add("Juan");
        nombres.add("María");
        nombres.add("Carlos");

        // Mostrar el contenido del ArrayList
        System.out.println("Nombres en la lista: " + nombres);
    }
}
